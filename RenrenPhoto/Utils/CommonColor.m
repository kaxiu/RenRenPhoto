#import "CommonColor.h"

@implementation CommonColor


//灰色 #b4b6b9
+(UIColor*)grayTextColor
{
    //return [UIColor colorWithRed:0xb4/255.0f green:0xb6/255.0f blue:0xb9/255.0f alpha:1.0f];
